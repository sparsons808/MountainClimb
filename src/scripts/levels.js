import Block from "./block";
import Lava from "./lava";

export function buildLevel(game, level) {
    // debugger
    let objects = [];

    level.forEach((row, rowIdx) => {
        row.forEach((object, objectIdx) => {
            if(object === 1) {
                let position = {
                    x: 24 * objectIdx,
                    y: 24 * rowIdx
                }

                objects.push(new Block(game, position));
            } else if(object === 2){
                let position = {
                    x: 100 * objectIdx,
                    y: 450
                }
                objects.push(new Lava(game, position));
            }
        });

    })
    // 21 blocks or rows in for the height of the canvas
    // 5 lava pits for the width of the canvas
    // 13 blocks or columns for the width of the canvas
    // put image source inside the level array
    return objects;
}

